
public class CalculaTabuadas {

	public static void main(String[] args) {
		
		for(int contador = 1; contador <= 10; contador++){
			System.out.println("");
			System.out.println("Tabuada "+contador);
			System.out.println("------------");
			
			for(int contadora = 1; contadora <= 10;contadora++){
				System.out.println(contador+" * "+contadora+" = "+contador*contadora);	
				}
			}
	}
}
